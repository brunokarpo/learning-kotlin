fun main(args: Array<String>) {

    var myFloat = 0.5
    println(myFloat)

    var newFloat: Float? = null
    println(newFloat)

    /*
     * We need to put this 'F' on the final of number to compiler understand that we are trying to put a Float inside the variable
     */
    newFloat = 23.4F
    println(newFloat)
}