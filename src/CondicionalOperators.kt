fun main(args: Array<String>) {

    val firstNumber = 5
    val secondNumber = 6

    println(firstNumber > secondNumber) // false
    println(firstNumber < secondNumber) // true
    println(firstNumber > firstNumber) // false
    println(firstNumber >= firstNumber) // true
    println(secondNumber < secondNumber) // false
